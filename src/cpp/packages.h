/**
 * @defgroup config Package 'config'
 *
 * The 'config' package contains components for reading and accessing the
 * data generator application configuration.
 */

/**
 * @defgroup core Package 'core'
 *
 * The 'core' package contains all core runtime and type components.
 */

/**
 * @defgroup core_types Package 'core.types'
 * @ingroup  core
 *
 * The 'core.type' package contains all core runtime and type components.
 */

/**
 * @defgroup communication Package 'communication'
 *
 * The 'communication' package contains all components required to implement
 * the CommunicationSubsystem.
 */

/**
 * @defgroup generator Package 'generator'
 *
 * The 'generator' package contains all components required to implement
 * the GeneratorSubsystem.
 */

/**
 * @defgroup io Package 'io'
 *
 * The 'io' package contains components required to output the generated data.
 */

/**
 * @defgroup math Package 'math'
 *
 * The 'math' package contains general purpose math computation functionality.
 */

/**
 * @defgroup math_algebra Package 'math.algebra'
 * @ingroup  math
 *
 * The 'math.algebra' package contains common algebra components that can be
 * used to generate and analyze sequences with particular value structure.
 */

/**
 * @defgroup math_probability Package 'math.probability'
 * @ingroup  math
 *
 * The 'math_probability' package contains all components required for working
 * with various probability distributions.
 */

/**
 * @defgroup math_random Package 'math.random'
 * @ingroup  math
 *
 * The 'math.random' package contains a definition of an abstract
 * parallelizeable pseudo-random number generator and several exchangeable
 * implementations with different performance footpring and cycle length.
 */

/**
 * @defgroup runtime Package 'runtime'
 *
 * The 'runtime' package contains all execution runtime components.
 */

/**
 * @defgroup runtime_predicate Package 'runtime.predicate'
 * @ingroup  runtime
 *
 * The 'runtime.predicate' package contains various predicate components.
 */

/**
 * @defgroup runtime_provider Package 'runtime.provider'
 * @ingroup  runtime
 *
 * The 'runtime.provider' package contains various provider components.
 */

/**
 * @defgroup runtime_provider_predicate Package 'runtime.provider.predicate'
 * @ingroup  runtime_provider
 *
 * The 'runtime.provider.predicate' package contains predicate binding
 * components.
 */

/**
 * @defgroup runtime_provider_range Package 'runtime.provider.range'
 * @ingroup  runtime_provider
 *
 * The 'runtime.provider.range' package contains runtime components that
 * provide value ranges.
 */

/**
 * @defgroup runtime_provider_reference Package 'runtime.provider.reference'
 * @ingroup  runtime_provider
 *
 * The 'runtime.provider.reference' package contains runtime components that
 * provide referenced records.
 */

/**
 * @defgroup runtime_provider_value Package 'runtime.provider.value'
 * @ingroup  runtime_provider
 *
 * The 'runtime.provider.value' package contains runtime components that
 * provide field values.
 */

/**
 * @defgroup runtime_setter Package 'runtime.setter'
 * @ingroup  runtime
 *
 * The 'runtime.setter' package contains components that are used to set
 * field values and references in the setter chain.
 */
