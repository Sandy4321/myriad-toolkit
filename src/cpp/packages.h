/**
 * @defgroup core Package 'core'
 *
 * The 'core' package conatins all core runtime and type components.
 */

/**
 * @defgroup config Package 'config'
 *
 * The 'config' package contains components for reading and accessing the
 * data generator application configuration.
 */

/**
 * @defgroup communication Package 'communication'
 *
 * The 'communication' package contains all components required to implement
 * the CommunicationSubsystem.
 */

/**
 * @defgroup generator Package 'generator'
 *
 * The 'generator' package contains all components required to implement
 * the GeneratorSubsystem.
 */

/**
 * @defgroup io Package 'io'
 *
 * The 'io' package contains components required to output the generated data.
 */

/**
 * @defgroup math Package 'math'
 *
 * The 'math' package contains general purpose math computation functionality.
 */

/**
 * @defgroup math_algebra Package 'math.algebra'
 * @ingroup  math
 *
 * The 'math.algebra' package contains common algebra components that can be
 * used to generate and analyze sequences with particular value structure.
 */

/**
 * @defgroup math_probability Package 'math.probability'
 * @ingroup  math
 *
 * The 'math_probability' package contains all components required for working
 * with various probability distributions.
 */

/**
 * @defgroup math_random Package 'math.random'
 * @ingroup  math
 *
 * The 'math.random' package contains a definition of an abstract
 * parallelizeable pseudo-random number generator and several exchangeable
 * implementations with different performance footpring and cycle length.
 */
